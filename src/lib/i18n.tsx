export const dictionaries: Record<SupportedLocale, Dictionary> = {
  'en-US': enUS,
  'zh-CN': zhCN,
  'de-DE': deDE,
  'ja-JP': jaJP,
  'ru-RU': ruRU,
};

export type I18nKey = TranslationKey;

export function getDictionaryKeys(dictionary: Record<string, string>): string[] {
  return Object.keys(dictionary).sort();
}

export function getLocaleName(locale: SupportedLocale): string {
  return localeNames[locale];
}

export function getLanguageMode(value: unknown): LanguageMode {
  if (value === DEFAULT_LANGUAGE_MODE || supportedLocales.includes(value as SupportedLocale)) {
    return value as LanguageMode;
  }
  return DEFAULT_LANGUAGE_MODE;
}

function readSettings(): Record<string, unknown> {
  try {
    const raw = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (!raw) return {};
    const parsed = JSON.parse(raw) as unknown;
    return parsed && typeof parsed === 'object' ? parsed as Record<string, unknown> : {};
  } catch {
    return {};
  }
}

export function loadLanguageMode(): LanguageMode {
  return getLanguageMode(readSettings().language);
}

export function saveLanguageMode(language: LanguageMode): void {
  const settings = readSettings();
  localStorage.setItem(LANGUAGE_STORAGE_KEY, JSON.stringify({ ...settings, language }));
}

export function resolveLocale(mode: LanguageMode, deviceLanguages: readonly string[] = []): SupportedLocale {
  if (mode !== DEFAULT_LANGUAGE_MODE) {
    return mode as SupportedLocale;
  }

  for (const language of deviceLanguages) {
    const normalized = language.toLowerCase();
    if (normalized.startsWith('zh')) return 'zh-CN';
    if (normalized.startsWith('de')) return 'de-DE';
    if (normalized.startsWith('ja')) return 'ja-JP';
    if (normalized.startsWith('ru')) return 'ru-RU';
    if (normalized.startsWith('en')) return 'en-US';
  }

  return DEFAULT_LOCALE;
}

function interpolate(template: string, values: Record<string, string | number> = {}): string {
  return template.replace(/\{\{\s*(\w+)\s*\}\}/g, (match, key: string) => {
    const value = values[key];
    return value === undefined ? match : String(value);
  });
}

export function t(
  key: string,
  options: { locale?: SupportedLocale; values?: Record<string, string | number> } = {},
): string {
  const locale = options.locale ?? DEFAULT_LOCALE;
  const dictionary = dictionaries[locale] as Record<string, string>;
  const fallback = dictionaries[DEFAULT_LOCALE] as Record<string, string>;
  return interpolate(dictionary[key] ?? fallback[key] ?? key, options.values);
}

interface I18nContextValue {
  languageMode: LanguageMode;
  locale: SupportedLocale;
  setLanguageMode: (language: LanguageMode) => void;
  t: (key: string, values?: Record<string, string | number>) => string;
  formatDateTime: (date: Date | string | number) => string;
}

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [languageMode, setLanguageModeState] = useState<LanguageMode>(() => loadLanguageMode());
  const [deviceLanguages, setDeviceLanguages] = useState<string[]>(() => Array.from(navigator.languages ?? [navigator.language]));

  useEffect(() => {
    const handleLanguageChange = () => {
      setDeviceLanguages(Array.from(navigator.languages ?? [navigator.language]));
    };

    window.addEventListener('languagechange', handleLanguageChange);
    return () => window.removeEventListener('languagechange', handleLanguageChange);
  }, []);

  const locale = useMemo(() => resolveLocale(languageMode, deviceLanguages), [languageMode, deviceLanguages]);

  const setLanguageMode = useCallback((language: LanguageMode) => {
    const normalized = getLanguageMode(language);
    saveLanguageMode(normalized);
    setLanguageModeState(normalized);
  }, []);

  const translate = useCallback(
    (key: string, values?: Record<string, string | number>) => t(key, { locale, values }),
    [locale],
  );

  const formatDateTime = useCallback(
    (date: Date | string | number) => new Date(date).toLocaleString(locale),
    [locale],
  );

  const value = useMemo<I18nContextValue>(
    () => ({
      languageMode,
      locale,
      setLanguageMode,
      t: translate,
      formatDateTime,
    }),
    [formatDateTime, languageMode, locale, setLanguageMode, translate],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const context = useContext(I18nContext);
  if (!context) {
    return {
      languageMode: DEFAULT_LANGUAGE_MODE,
      locale: DEFAULT_LOCALE,
      setLanguageMode: () => undefined,
      t: (key, values) => t(key, { locale: DEFAULT_LOCALE, values }),
      formatDateTime: (value) => new Intl.DateTimeFormat(DEFAULT_LOCALE, {
        dateStyle: 'medium',
        timeStyle: 'short',
      }).format(new Date(value)),
    };
  }
  return context;
}
